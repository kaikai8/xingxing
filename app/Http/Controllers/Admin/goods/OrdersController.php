     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {   $res = Orders::orderBy('oid','asc')->where(function($query) use($request){
                
                
                $ord = $request->input('ord');
                
                if(!empty($ord)) {
                    $query->where('ord','like','%'.$ord.'%');
                }
            })
            ->paginate($request->input('num', 10));
    
        return view('admin.orders.orders',['title'=>'订单管理','res'=>$res,'request'=>$request]);
    }

